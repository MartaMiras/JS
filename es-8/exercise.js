//Cree una variable y configúrela como true.
let inventada = true;

//Imprime el valor en la consola y el tipo de dato.
console.log("Value:", inventada);
console.log("Data type:", typeof inventada);

//Conviértalo a un tipo de dato numérico, imprima el valor y el tipo de dato.
inventada = Number(inventada);
console.log("Value:", inventada);
console.log("Data type:", typeof inventada);

//Conviértalo a tipo de datos de cadena, imprima el valor y el tipo de datos.
inventada = String(inventada);
console.log("Value:", inventada);
console.log("Data type:", typeof inventada);

//Finalmente, conviértalo a un tipo de datos booleano, imprima el valor y el tipo de datos.
inventada = Boolean(inventada);
console.log("Value:", inventada);
console.log("Data type:", typeof inventada);
